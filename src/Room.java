
public class Room
{
	int roomNumber;
	double costPerNight;
	boolean reserved;
	int roomType;
	
	
	public Room(int roomNumber, double costPerNight, boolean reserved, int roomType)
	{
				
		this.roomNumber = roomNumber;
		this.costPerNight = costPerNight;
		this.reserved = reserved;
		this.roomType = roomType;
	}
	
	

}
